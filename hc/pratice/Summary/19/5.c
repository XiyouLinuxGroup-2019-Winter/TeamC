#include <stdio.h>

int main(int argc, char *argv[]) 
{
   // 默认为signed char------> 对应整数-128～127
    char ch = 255;
    //255的原码0000 0000 ..... 1111 1111
    //255的补码0000 0000 ..... 1111 1111
    //char占一个字节也就是8位
    //取255的一个字节1111 1111
    //然后可以看出把255看成一个char型的时候，是一个负数，接下来求出负数的原码
    //1000 0001     ------> -1
    int d = ch + 1;
    //与上面同理
    //256取一个字节 0000 0000 ------> 0
    printf("%d %d", ch, d);
}
